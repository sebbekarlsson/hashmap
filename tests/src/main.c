void test_map_unset()
{
  map_T* map = NEW_MAP();
  map_set(map, "xyz", strdup("123"));

  map_bucket_T* bucket = map_get(map, "xyz");
  ASSERT(bucket != 0, "item exists.");

  map_unset(map, "xyz");

  bucket = map_get(map, "xyz");
  ASSERT(bucket == 0, "item is removed.");
}

void test_map_unset_then_set()
{
  map_T* map = NEW_MAP();

  // set
  map_set(map, "xyz", strdup("123"));
  map_bucket_T* bucket = map_get(map, "xyz");
  ASSERT(bucket != 0, "item exists.");

  // unset
  map_unset(map, "xyz");

  // assert not exist
  bucket = map_get(map, "xyz");
  ASSERT(bucket == 0, "item is removed.");

  // assert exists again
  map_set(map, "xyz", strdup("123"));
  bucket = map_get(map, "xyz");
  ASSERT(bucket != 0, "item exists again.");
}

