void test_map_change_value()
{
  map_T* map = NEW_MAP();
  map_set(map, "name", strdup("ianertson"));
  map_set(map, "github", strdup("github.com/sebbekarlsson"));
  map_set(map, "age", strdup("23"));

  map_bucket_T* bucket = map_get(map, "name");
  if (!ASSERT(bucket != 0, "item exists."))
    return;
  ASSERT(strcmp(bucket->value, "ianertson") == 0, "value is correct");

  map_set(map, "name", "sebastian");

  bucket = map_get(map, "name");
  ASSERT(bucket != 0, "changed item exists.");
  ASSERT(strcmp(bucket->value, "sebastian") == 0, "value has changed");
}

